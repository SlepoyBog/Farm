def get_engagement_question(niche: str, title: str = "") -> str:
    pool = QUESTIONS.get(niche, QUESTIONS["default"])
    idx = (len(title) if title else 0) % len(pool)
    return pool[idx]


def get_poll(niche: str, title: str = "") -> tuple[str, list[str]] | None:
    pool = POLL_TOPICS.get(niche, POLL_TOPICS.get("default", []))
    if not pool:
        return None
    idx = (len(title) if title else 0) % len(pool)
    return pool[idx]


def inject_into_tg_prompt(prompt_template: str, niche: str, title: str = "") -> str:
    question = get_engagement_question(niche, title)
    hook = (
        "\n\nВАЖНО: Закончи пост вовлекающим вопросом к аудитории. "
        "Вопрос должен быть по теме и побуждать к комментарию.\n"
        "Пример вопроса: \"%s\"" % question
    )
    return prompt_template + hook


def enhance_post_text(text: str, niche: str, title: str = "") -> str:
    question = get_engagement_question(niche, title)
    text = text.rstrip()
    if text and not text.endswith((".", "!", "?", "…", "\"", ")")):
        text += "."
    return f"{text}\n\n💬 {question}"


